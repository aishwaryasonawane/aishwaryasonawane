package EncapsulationPack;

public class EncapsulationExample 
{
	private int roll_no;
	private String name;
	
	public void setRoll_no(int roll_no)
	{
		this.roll_no=roll_no;
	}
	
	public void setName(String name)
	{
		this.name=name;
	}
	
	public int getRoll_no()
	{
		return roll_no;
	}
	
	public String getName()
	{
		return name;
	}

}
